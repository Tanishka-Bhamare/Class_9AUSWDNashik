class dec_val
{
    public static void main ()
    {
        int x='A',y='a',z=' ',q='_',r='0',s='$';
        System.out.println("Integer value for A="+(x));
        System.out.println("Integer value for a="+(y));
        System.out.println("Integer value for space="+(z));
        System.out.println("Integer value for underscore="+(q));
        System.out.println("Integer value for zero="+(r));
        System.out.println("Integer value for $="+(s));
        System.out.println("Integer value of A+2="+(x+2));
    }
}